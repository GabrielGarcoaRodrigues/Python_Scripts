# Tipos int e float

# int -> Números inteiros
# O tipo int representa qualquer número inteiro, positivo ou negativo, sem casas decimais.
print(11) # int
print(-11) # int

# float -> Números com ponto flutuante
# O tipo float representa qualquer número, positivo ou negativo, com casas decimais.
print(1.2, 10.5) # floar
print(-1.2) # float

# A função type() retorna o tipo de um valor ou variável.
print(     )
print( type(1) ) # -> int
print( type(1.2) ) # -> float
print( type('Gabriel') ) # -> str
